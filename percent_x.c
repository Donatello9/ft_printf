#include <unistd.h>

void	percent_x(int n)
{
	char	tab[] = "0123456789ABCDEF";
	long	nb;
	char	c;

	nb = n;
	if (nb >= 16)
	{
		percent_x(nb / 16);
	}
	c = tab[nb % 16];
	write(1, &c, 1);
}
