#include "libprintf.h"

int	percent_d(int n)
{
	long nb;
	int s;
	char c;

	s = 0;
	nb = n;
	if (nb < 0)
	{
		s += write(1, "-", 1);
		nb = -nb;
	}
	if (nb > 9)
	{
		s += percent_d(nb / 10);
	}
	c = nb % 10 + '0';
	s += write(1, &c, 1);
	return (s);
}