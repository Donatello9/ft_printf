#include "libprintf.h"

int	percent_u(unsigned int n)
{
	int		s;
	char	c;

	s = 0;
	if (n >= 10)
	{
		s += ft_print_u(n / 10);
		s += ft_print_u(n % 10);
	}
	else
	{
		c = n + '0';
		s += write(1, &c, 1);
	}
	return (s);
}
